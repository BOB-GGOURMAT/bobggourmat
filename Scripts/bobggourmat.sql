ALTER TABLE category convert to charset utf8;
drop table category;
select * from category;
create table category (
	category_idx	int AUTO_INCREMENT PRIMARY KEY,
	category_name	varchar(100)
);

ALTER TABLE location  convert to charset utf8;
drop table location;
select * from location;
create table location(
   location_idx int AUTO_INCREMENT PRIMARY KEY,
   location_name varchar(50)
);

alter TABLE resinfo  convert to charset utf8;
drop table resinfo;
select * from resinfo;
create table resinfo(
   resinfo_idx   int AUTO_INCREMENT PRIMARY KEY,
   resinfo_name varchar(50),
   resinfo_address varchar(100),
   resinfo_tel varchar(50),
   resinfo_review varchar(1000),
   location_idx int,
   category_idx int,
   foreign key (location_idx) references location(location_idx),
   foreign key (category_idx) references category(category_idx),
   resinfo_mainphoto varchar(500),
   resinfo_subphoto1 varchar(500),
   resinfo_subphoto2 varchar(500),
   resinfo_map       varchar(100)
);

alter table menu convert to charset utf8;
drop table menu;
select * from menu;
create table menu(
   menu_idx        int    auto_increment  primary key,
   resinfo_idx int,
   foreign key (resinfo_idx) references resinfo(resinfo_idx),
   menu_name       varchar(50) ,
   menu_price      varchar(20) 
  );
 
 
alter table user convert to charset utf8;
drop table user;
select * from user;
create table user(
	user_idx        int    auto_increment  primary key,
	user_name       varchar(10) not null,
	user_nickname   varchar(50)	not null,
	user_id         varchar(50) not null,
	user_password	varchar(50)	not null,
	user_email	    varchar(50)	not null,
	user_phone	    varchar(50)	not NULL,
	user_icon       varchar(50) DEFAULT '#ffce49'
	);

alter table comment convert to charset utf8;
drop table comment;
select * from comment;
create table comment (
	comment_idx		int AUTO_INCREMENT PRIMARY KEY,
	user_idx int,
	resinfo_idx int,
	foreign key (user_idx) REFERENCES user(user_idx),
    foreign key (resinfo_idx) REFERENCES resinfo(resinfo_idx),
	comment_time	timestamp DEFAULT NOW(),
	comment_content varchar(500) not null,
	comment_star	int not null
);

drop table likes;
select * from likes;
create table likes(
    user_idx int,
    comment_idx int,
    foreign key (user_idx) references user(user_idx),
    foreign key (comment_idx) references comment(comment_idx)
);

drop table save;
select * from save;
create table save(
    user_idx int,
    resinfo_idx int,
    foreign key (user_idx) references user(user_idx),
    foreign key (resinfo_idx) references resinfo (resinfo_idx)
);

